#include <stdio.h>
#include "ft_printf.h"

int main()
{
	ft_printf("[%#010.0lf]\n", -6.393847);
	printf("[%#010.0lf]\n", -6.393847);

	ft_printf("[%#.f]\n",   3.141593);
	printf("[%#.f]\n",   3.141593);

	ft_printf("[%#.f]\n",   -3.141593);
	printf("[%#.f]\n",   -3.141593);

	ft_printf("%-10.f\n",  -6.393847);
	printf("%-10.f\n",  -6.393847);

	ft_printf("%10f\n",  -6.393847);
	printf("%10f\n",  -6.393847);

	ft_printf("%10f\n",  6.393847);
	printf("%10f\n",  6.393847);
	return 0;
}